public class SuffixConst {
    public static void main(String[] args)
    {
        double e1 = 7.125;
        float e2 = 7.125F;

        long n1 = 10000000000L;
        long n2 = 150;

        System.out.println(e1);
        System.out.println(n1);
    }
}


